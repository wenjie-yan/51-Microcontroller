#include <REGX52.H>
#include "Delay.h"
#include "MatrixLED.h"

unsigned char animation[]={0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0xFF,0x10,0x10,0x10,0xFF,0x00,0xFF,0x91,0x91,0x91,0x91,0x00,0xFF,0x01,0x01,0x01,
0x00,0xFF,0x01,0x01,0x01,0x00,0xFF,0x81,0x81,0x81,0xFF,0x00,0xFD,0x00,0xFD,0x00,
0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00};

void main()
{
	unsigned char i,offset=0,count=0;
	MatrixLED_Init();
	while(1)
	{
		for(i=0;i<8;i++)
		{
			MatrixLED_ShowColumn(i,animation[i+offset]);
		}
		count++;
		if(count>10)
		{
			count=0;
			offset++;
			if(offset>32)
			{
				offset=0;
			}
		}
	}
}